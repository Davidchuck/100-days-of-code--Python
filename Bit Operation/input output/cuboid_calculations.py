#Calculating the surface area, and volume of a cuboid

print("The following instruction will guide in calculating the Surface are of a cuboid")
print("Enter the length")
l=float(input())

print("Enter the Width")
w=float(input())

print("Enter the Height")
h=float(input())

SA=2*(l*w) + 2*(w*h) + 2*(h*w)

print("The surface area is ", SA, "square units") 